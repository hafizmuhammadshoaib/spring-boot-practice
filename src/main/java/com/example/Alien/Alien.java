package com.example.Alien;

public class Alien {

    private String id;

    private String aname;

    private String tech;

    public String getAname() {
        return aname;
    }

    public void setAname(String aname) {
        this.aname = aname;
    }

    public String getTech() {
        return tech;
    }

    public void setTech(String tech) {
        this.tech = tech;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

}